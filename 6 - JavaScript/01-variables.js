// Variables - store values (container)
// let, const, var (var is not beginner friendly, does the same thing as let)

// let - mutable/editable value
let firstName = "Jared";
console.log("Firstname:", firstName);

firstName = "Ariston";
console.log("Firstname:", firstName);

// const - immutable/uneditable value
const lastName = "Hortaleza";
console.log("Lastname:", lastName);

