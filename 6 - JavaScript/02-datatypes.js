// Data Types - Primitive Types (Dont complicate things, for singular value)

// Number: Represent numeric values (35, 3.14)
let age = -22.3;
console.log("Age: " , age);
console.log("Typeof age:", typeof age);

//strings: Represent textual values ("abc", 'def123')
let name = "John";
console.log("Name: " , name);
console.log("Typeof name:", typeof name);

//Boolean: Either true or false
let isAdmin = true;
console.log("isAdmin: " , isAdmin);
console.log("Typeof isAdmin:", typeof isAdmin);

// Null: Represent INTENTIONAl absence of a value
let car = null;
console.log("Car: " , car);
console.log("Typeof car:", typeof car);

// Undefined: Representing UNINTENTIONAL absent of value
let city;
console.log("city?" , city);
console.log("Typeof city:", typeof city);
